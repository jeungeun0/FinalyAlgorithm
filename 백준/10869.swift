import Foundation

let input = readLine()!
let inputArr = input.split(separator: " ")

let output1 = Int(inputArr[0])! + Int(inputArr[1])!
let output2 = Int(inputArr[0])! - Int(inputArr[1])!
let output3 = Int(inputArr[0])! * Int(inputArr[1])!
let output4 = Int(inputArr[0])! / Int(inputArr[1])!
let output5 = Int(inputArr[0])! % Int(inputArr[1])!

print(output1)
print(output2)
print(output3)
print(output4)
print(output5)
